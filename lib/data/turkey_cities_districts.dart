class TurkeyProvince {
  const TurkeyProvince({
    required this.name,
    required this.districts,
  });

  final String name;
  final List<String> districts;
}

class TurkeyLocationSelection {
  const TurkeyLocationSelection({
    required this.province,
    this.district,
  });

  static const String separator = ' / ';

  final String province;
  final String? district;

  String get displayName {
    final selectedDistrict = district;
    if (selectedDistrict == null || selectedDistrict.isEmpty) {
      return province;
    }

    return '$province$separator$selectedDistrict';
  }

  String get apiCity {
    final selectedDistrict = district;
    if (selectedDistrict == null || selectedDistrict.isEmpty) {
      return province;
    }

    if (normalizeTurkeyLocationText(selectedDistrict) == 'merkez') {
      return province;
    }

    return selectedDistrict;
  }

  static TurkeyLocationSelection? tryParse(String value) {
    final parts = value
        .split('/')
        .map((part) => part.trim())
        .where((part) => part.isNotEmpty)
        .toList();

    if (parts.isEmpty) {
      return null;
    }

    if (parts.length >= 2) {
      final province = _findProvinceName(parts.first);
      if (province == null) {
        return null;
      }

      final district = _findDistrictName(province, parts[1]);
      if (district == null) {
        return null;
      }

      return TurkeyLocationSelection(province: province, district: district);
    }

    final province = _findProvinceName(parts.first);
    if (province == null) {
      return null;
    }

    return TurkeyLocationSelection(province: province);
  }
}

