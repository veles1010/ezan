List<String> get turkeyProvinceNames {
  return List<String>.unmodifiable(
    turkeyProvinces.map((province) => province.name),
  );
}

TurkeyProvince? findTurkeyProvince(String provinceName) {
  final normalizedProvinceName = normalizeTurkeyLocationText(provinceName);
  for (final province in turkeyProvinces) {
    if (normalizeTurkeyLocationText(province.name) == normalizedProvinceName) {
      return province;
    }
  }

  return null;
}

bool isSupportedTurkeyLocation(String value) {
  return TurkeyLocationSelection.tryParse(value) != null;
}

String normalizeTurkeyLocationText(String value) {
  return value
      .toLowerCase()
      .replaceAll('\u0307', '')
      .replaceAll('ç', 'c')
      .replaceAll('ğ', 'g')
      .replaceAll('ı', 'i')
      .replaceAll('ö', 'o')
      .replaceAll('ş', 's')
      .replaceAll('ü', 'u')
      .trim();
}

String? _findProvinceName(String value) {
  return findTurkeyProvince(value)?.name;
}

String? _findDistrictName(String provinceName, String districtName) {
  final province = findTurkeyProvince(provinceName);
  if (province == null) {
    return null;
  }

  final normalizedDistrictName = normalizeTurkeyLocationText(districtName);
  for (final district in province.districts) {
    if (normalizeTurkeyLocationText(district) == normalizedDistrictName) {
      return district;
    }
  }

  return null;
}
